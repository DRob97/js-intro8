/*
Escape Sequences

\n Inserts a new line
\t Inserts a tab space
\' Inserts a single quote into text
\\ Inserts ONE backslash per double backslash
\" Inserts a double quote into text
*/

// Output I like "Apple".

console.log("I like \"Apple\".");
console.log('I like "Apple".');

// Output I like 'Oranges'.

console.log('I like \'Oranges\'.');
console.log("I like 'Oranges'.");

/*
    JavaScript is an interpreted language and it is used
commonly for web applications
*/

console.log("\tJavaScript is an interpreted language and it is used\ncommonly for web applications.");
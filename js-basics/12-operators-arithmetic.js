// assignment operator: =
let num1 = 10, num2 = 5, num3 = 3, num4 = 7, num5 = 2;

//addition
console.log(num1 + num5);                                   // 12
console.log(num2 + num3 + num4);                            // 15

//subtraction
console.log(num1 - num4);                                   // 3
console.log(num3 - num4 - num1);                            // -14

//multiplication
console.log(num5 * num3);                                   // 6
console.log(num1 * num2 * num3 * num4 * num5);              // 2100

//division
console.log(num1 / num5);                                   // 5
console.log(num1 / num2);                                   // 2

//exponentiation
console.log(num1 ** num3);                                  // 1000
console.log(num4 ** num5);                                  // 49

//modulus (remainder)
console.log(num1 % num2);                                   // 0
console.log(num1 % num3);                                   // 1
console.log(num5 % num4);                                   // 2

